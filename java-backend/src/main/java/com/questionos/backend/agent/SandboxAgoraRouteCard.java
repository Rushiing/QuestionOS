    /** 审议室中文名（与路由卡片一致） */
    public static String roomTitle(SandboxDeliberationScene scene) {
        return roomFor(scene).roomTitle();
    }

    /** 审议室副标题 */
    public static String roomSubtitle(SandboxDeliberationScene scene) {
        return roomFor(scene).subtitle();
    }
